package com.example.seatplanner;

public abstract class Table {
    private double x;
    private double y;
    private double width;
    private double height;
    private double capacity;

    public Table(double x, double y, double width, double height, double capacity) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;

    }



}
